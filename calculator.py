def add(num1, num2):
    return (num1 + num2)
def subtract(num1, num2):
    return (num1 - num2)
def multiply(num5, num2):
    return (num5 * num2)
def divide(num1, num2):
    if num1 >0 and num2 > 0:
        return (num1 / num2)
def power(num1, num2):
    return (num1 ** num2)
def square_root(num1):
    if num1 >= 0:
        return num1 ** 0.5
